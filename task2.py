# Have the user input a number 
# Determine if the number is positive, negative or 0 
# 2 points

# Inputs:
# number

# Outputs:
# - "positive"
# - "negative"
# - "zero"
a = input("The number is:")

a = int(a)

number = 0

number = int(number)

if a > number:
    print("The number is positive")

if a < number:
    print("The number is negative")

if a == number:
    print("The number is zero")